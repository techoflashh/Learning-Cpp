#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    // max and min function can be used without the header file cmath
    cout << "max(5, 7) =" << max(5, 7) << endl;
    cout << "min(5, 7) = " << min(5, 7) << endl;
    cout << "sqrt(64) = " << sqrt(64) << endl;
    cout << "log(10) = " << log(10) << endl;
    cout << "log10(100) = " << log10(100) << endl;
    cout << "round(2.6) = " << round(2.6) << endl;
    return 0;
}